package Fundamentals;

public class ControlFlow {
    public static void main(String[] args) {

        int age = 23;

        if (age >= 20) {
            System.out.println("Not a teen anymore");
        } else if (age >= 16) {
            System.out.println("You're Good");
        } else {
            System.out.println("Hehe");
        }

    }
}
